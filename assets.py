    @staticmethod
    def get_icon(name, size=24, color="white"):
        """Get icon as CTkImage with caching"""
        cache_key = f"{name}_{size}_{color}"
        if cache_key in IconManager._cache:
            return IconManager._cache[cache_key]
            
        if name not in IconManager.ICONS:
            name = "help"  # Fallback
            
        svg_content = IconManager.ICONS[name].replace("currentColor", color)
        
        # Convert SVG to PNG bytes using resvg
        png_bytes = svg_to_bytes(svg_content, width=size, height=size)
        
        # Create PIL Image
        img = Image.open(io.BytesIO(png_bytes))
        
        # Return and cache CTkImage
        ctk_img = ctk.CTkImage(light_image=img, dark_image=img, size=(size, size))
        IconManager._cache[cache_key] = ctk_img
        return ctk_img

    @staticmethod
    def export_icon(name, output_path, size=256, color="#3b82f6"):
        """Export icon to a file (PNG or ICO)"""
        if name not in IconManager.ICONS:
            return False
            
        svg_content = IconManager.ICONS[name].replace("currentColor", color)
        
        # Convert SVG to PNG bytes
        png_bytes = svg_to_bytes(svg_content, width=size, height=size)
        
        # Create PIL Image
        img = Image.open(io.BytesIO(png_bytes))
        
        # Save based on extension
        if str(output_path).lower().endswith(".ico"):
            img.save(output_path, format="ICO", sizes=[(16, 16), (32, 32), (48, 48), (64, 64), (128, 128), (256, 256)])
        else:
            img.save(output_path, format="PNG")
        
        return True
